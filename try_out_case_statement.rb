weather_station = 'sunny'

case weather_station
when 'sunny'
	puts "it's a sunny day. wear shades."
when 'cloudy'
	puts "it may be cold, wear a light coat."
when 'rainy'
	puts "wear a rain coat or bring an umbrella."
when 'snowy'
	puts "wear snow attire."
when 'thunder'
	puts "stay inside."
when 'haily'
	puts "play rain day games inside."
else 
	puts "just make the best of the weather!"
end